var agora = new Date()
var diaSem = agora.getDay()


switch(diaSem) {
    case 0:
        console.log('Domingo')
        break
    case 0:
        console.log('Segunda')
        break
    case 0:
        console.log('Terça')
        break
    case 0:
        console.log('Quarta')
        break   
    case 0:
        console.log('Quinta')
        break
    case 0:
        console.log('Sexta')
        break
    case 0:
        console.log('Sábado')
        break
    default:
        console.log('[Erro] Dia Inválido')
        break         
}